/* start_Task_LED function */
void start_Task_LED(void const * argument)
{
  /* USER CODE BEGIN start_Task_LED */
  /* Infinite loop */
  for(;;)
  {
		int T = 0;
		for (;;) {
			if (etat_LED == 0) {
				LED_ON_NUCLEO;
				LED_ON_OUT;
				osDelay(1000);
			} else {
				if (etat_LED == 1) {
					T = 1000;
				}
				if (etat_LED == 2) {
					T = 200;
				}
				LED_ON_NUCLEO;
				LED_ON_OUT;
				osDelay(T / 2);
				LED_OFF_NUCLEO;
				LED_OFF_OUT;
				osDelay(T / 2);
			}
		}
  }
  /* USER CODE END start_Task_LED */
}

