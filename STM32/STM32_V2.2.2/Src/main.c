float b[2] = {1.126, -1.085};
float a[2] = {1, -1};
float e[2] = {0, 0};
float s[2] = {0, 0};
float u;

float REGUL(float cmd_manette, float ADC_value)
{

	/* MAJ e et s*/
	e[1]=e[0];
	e[0]=cmd_manette;

	s[1]=s[0];
	s[0]=ADC_value;

	/* Calcul de la commande */

	u = (1/a[0])*( (b[0]*(e[0]-s[0]) + b[1]*(e[1]-s[1]) - a[1]*u));

	return u;
}
